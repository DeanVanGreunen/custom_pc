//! Decoded instruction type for the CPU executor.

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Instruction {
    Nop,
    Hlt,
    Mov  { rd: u8, rs: u8 },
    Ldi  { rd: u8, imm: u16 },
    Ld   { rd: u8, rs: u8 },
    St   { rd: u8, rs: u8 },
    Ldb  { rd: u8, rs: u8 },
    Stb  { rd: u8, rs: u8 },
    Add  { rd: u8, rs: u8 },
    Sub  { rd: u8, rs: u8 },
    And  { rd: u8, rs: u8 },
    Or   { rd: u8, rs: u8 },
    Xor  { rd: u8, rs: u8 },
    Not  { rd: u8 },
    Shl  { rd: u8, rs: u8 },
    Shr  { rd: u8, rs: u8 },
    Addi { rd: u8, imm: u16 },
    Subi { rd: u8, imm: u16 },
    Andi { rd: u8, imm: u16 },
    Ori  { rd: u8, imm: u16 },
    Xori { rd: u8, imm: u16 },
    Cmp  { rd: u8, rs: u8 },
    Cmpi { rd: u8, imm: u16 },
    Push { rs: u8 },
    Pop  { rd: u8 },
    Call { addr: u16 },
    Ret,
    Jmp  { addr: u16 },
    Jz   { addr: u16 },
    Jnz  { addr: u16 },
    Jc   { addr: u16 },
    Jnc  { addr: u16 },
    Jn   { addr: u16 },
    Jnn  { addr: u16 },
    Jv   { addr: u16 },
    Jnv  { addr: u16 },
    Jgt  { addr: u16 },
    Jlt  { addr: u16 },
    Jge  { addr: u16 },
    Jle  { addr: u16 },
    In   { rd: u8, port: u16 },
    Out  { rs: u8, port: u16 },
}
