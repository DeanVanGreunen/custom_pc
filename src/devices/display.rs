//! Display devices: text-mode and pixel-mode (14-bit ARGB).
//!
//! Memory map:
//!   0xA000–0xBFFF  8 KiB bank window into the pixel framebuffer
//!   0xC000+        Text-mode framebuffer (FB_COLS × FB_ROWS cells, 2 bytes each)
//!   0xCFA0         REG_DISP_MODE  — 0 = text, 1 = pixel
//!   0xCFA1         REG_FB_BANK    — active 8 KiB bank (0–FB_PIXEL_BANKS-1)
//!   0xCFA2         REG_DISP_CTRL  — bit0 = vsync-en, bit1 = clear (self-clearing)

// ── Screen / pixel-mode dimensions ───────────────────────────────────────────

pub const SCREEN_W: usize = 1024;
pub const SCREEN_H: usize = 720;
pub const PIXEL_COUNT: usize = SCREEN_W * SCREEN_H;

// ── Text-mode layout ─────────────────────────────────────────────────────────

pub const FB_BASE: u32      = 0xC000;
pub const CELL_WIDTH: usize = 8;   // glyph pixels wide  (matches FONT_8X8 width)
pub const CELL_HEIGHT: usize = 16; // cell pixels tall   (font rows are doubled)
pub const FB_COLS: usize    = SCREEN_W / CELL_WIDTH;   // 128
pub const FB_ROWS: usize    = SCREEN_H / CELL_HEIGHT;  // 45
pub const FB_SIZE: usize    = FB_COLS * FB_ROWS * 2;

