pub struct Display;

impl Display {
    /// Read the text framebuffer and return each cell as `(char, fg_4bit, bg_3bit)`.
    pub fn render(mem: &[u8]) -> Vec<(char, u8, u8)> {
        let start = FB_BASE as usize;
        let end   = (start + FB_SIZE).min(mem.len());
        mem[start..end]
            .chunks_exact(2)
            .map(|cell| {
                let ch   = cell[0];
                let attr = cell[1];
                let fg   = attr & 0x0F;
                let bg   = (attr >> 4) & 0x07;
                let c    = if ch.is_ascii_graphic() || ch == b' ' { ch as char } else { ' ' };
                (c, fg, bg)
            })
            .collect()
    }

    pub fn dump_text(mem: &[u8]) -> String {
        let cells = Self::render(mem);
        let mut out = String::new();
        for row in cells.chunks(FB_COLS) {
            for (c, _, _) in row { out.push(*c); }
            out.push('\n');
        }
        out
    }

    /// Rasterise the text framebuffer into a `SCREEN_W × SCREEN_H` pixel buffer
    /// (`0x00RRGGBB`) for minifb.
    ///
    /// Each character cell is `CELL_WIDTH × CELL_HEIGHT` pixels. The 8-row font
    /// glyph is vertically doubled (each row emitted twice) to fill `CELL_HEIGHT`.
    pub fn render_to_pixels(mem: &[u8]) -> Vec<u32> {
        let mut buf = vec![0u32; SCREEN_W * SCREEN_H];
        let fb_start = FB_BASE as usize;
        let fb_end   = (fb_start + FB_SIZE).min(mem.len());

        for row in 0..FB_ROWS {
            for col in 0..FB_COLS {
                let idx = fb_start + (row * FB_COLS + col) * 2;
                if idx + 1 >= fb_end { break; }

                let ch    = mem[idx] as usize & 0x7F;
                let attr  = mem[idx + 1];
                let fg    = CGA_PALETTE[(attr & 0x0F) as usize];
                let bg    = CGA_PALETTE[((attr >> 4) & 0x07) as usize];
                let glyph = &FONT_8X8[ch];

                let px0 = col * CELL_WIDTH;
                let py0 = row * CELL_HEIGHT;

                for gy in 0..8usize {
                    let row_byte = glyph[gy];
                    for y_sub in 0..2usize {
                        let py = py0 + gy * 2 + y_sub;
                        for gx in 0..CELL_WIDTH {
                            let lit = (row_byte >> gx) & 1 != 0;
                            let px = px0 + gx;
                            if py < SCREEN_H {
                                buf[py * SCREEN_W + px] = if lit { fg } else { bg };
                            }
                        }
                    }
                }
            }
        }

        buf
    }
}

// ── Pixel mode ────────────────────────────────────────────────────────────────

pub const BYTES_PER_PIXEL: usize = 2;
pub const PIXEL_FB_SIZE: usize   = SCREEN_W * SCREEN_H * BYTES_PER_PIXEL;

pub const FB_PIXEL_BASE:   u32   = 0xA000;
pub const FB_PIXEL_END:    u32   = 0xBFFF;
pub const FB_PIXEL_WINDOW: usize = 0x2000;
pub const FB_PIXEL_BANKS:  usize = PIXEL_FB_SIZE.div_ceil(FB_PIXEL_WINDOW);

pub const REG_DISP_MODE: u32 = 0xCFA0;
pub const REG_FB_BANK:   u32 = 0xCFA1;
pub const REG_DISP_CTRL: u32 = 0xCFA2;

/// Pixel format (14-bit ARGB packed into a u16):
///   bits 13–12  Alpha (0=transparent … 3=opaque)
///   bits 11– 8  Red   (0–15)
///   bits  7– 4  Green (0–15)
///   bits  3– 0  Blue  (0–15)
#[derive(Clone)]
pub struct PixelDisplay {
    pub framebuffer: Vec<u16>,
    pub bank: u8,
    pub mode: u8,
    pub ctrl: u8,
}

impl PixelDisplay {
    pub fn new() -> Self {
        Self { framebuffer: vec![0u16; PIXEL_COUNT], bank: 0, mode: 0, ctrl: 0 }
    }

    pub fn write_byte(&mut self, addr: u32, val: u8) {
        let offset    = (addr - FB_PIXEL_BASE) as usize;
        let byte_pos  = self.bank as usize * FB_PIXEL_WINDOW + offset;
        let pixel_idx = byte_pos / 2;
        if pixel_idx >= PIXEL_COUNT { return; }
        if byte_pos % 2 == 0 {
            self.framebuffer[pixel_idx] = (self.framebuffer[pixel_idx] & 0xFF00) | val as u16;
        } else {
            self.framebuffer[pixel_idx] = (self.framebuffer[pixel_idx] & 0x00FF) | ((val as u16) << 8);
        }
    }

    pub fn read_byte(&self, addr: u32) -> u8 {
        let offset    = (addr - FB_PIXEL_BASE) as usize;
        let byte_pos  = self.bank as usize * FB_PIXEL_WINDOW + offset;
        let pixel_idx = byte_pos / 2;
        if pixel_idx >= PIXEL_COUNT { return 0; }
        if byte_pos % 2 == 0 {
            (self.framebuffer[pixel_idx] & 0xFF) as u8
        } else {
            (self.framebuffer[pixel_idx] >> 8) as u8
        }
    }

    #[inline]
    pub fn decode_pixel(px: u16) -> (u8, u8, u8, u8) {
        let a = ((px >> 12) & 0x3) as u8;
        let r = ((px >>  8) & 0xF) as u8;
        let g = ((px >>  4) & 0xF) as u8;
        let b = ( px        & 0xF) as u8;
        (a, r, g, b)
    }

    #[inline]
    pub fn to_rgba8(px: u16) -> (u8, u8, u8, u8) {
        let (a, r, g, b) = Self::decode_pixel(px);
        (r * 17, g * 17, b * 17, a * 85)
    }

    pub fn to_argb32(&self) -> Vec<u32> {
        self.framebuffer.iter().map(|&px| {
            let (_, r, g, b) = Self::to_rgba8(px);
            ((r as u32) << 16) | ((g as u32) << 8) | (b as u32)
        }).collect()
    }

    pub fn clear(&mut self) { self.framebuffer.fill(0); }
}

impl Default for PixelDisplay {
    fn default() -> Self { Self::new() }
}
